	<h1> -- DRAFT -- </h1>
	<h1>The Dual History of the Swastika and Swastik</h1>
	<p>
		The symbol often referred to in the West as the "swastika" has a rich and multifaceted history
		that spans cultures, continents, and millennia.
	</p>
	<h2>The Ancient and Auspicious Swastik:</h2>
	<p>
		Originating in the Indian subcontinent, the स्वास्तिक, transliterated as "swastik" (other
		transliterations include: "svastik", "swaastik", "svaastik", "svastika", "swastika", "swaastika",
		and "svaastika") is a symbol of auspiciousness, prosperity, and good fortune. Found in Hindu
		temples, scriptures, and homes, it is often used to mark the beginning of religious texts,
		ceremonies, and festivals.
	</p>
	<h2>The Swastika in Other Cultures:</h2>
	<p>
		Beyond Hinduism, the swastika has been found in ancient cultures all over the world, from the
		Native American tribes to ancient Greeks, Celts, and even in early Christian art. In each context,
		its meaning varies but generally hovers around themes of eternity, cyclical patterns, and good
		fortune.
	</p>
	<h2>The Dark Era of the Swastika:</h2>
	<p>
		In the early 20th century, the swastika was co-opted by the Nazi party in Germany, twisting its
		meaning from one of positivity and life to one of hate, anti-Semitism, and death. For millions
		around the world, especially the Jewish community, the symbol became a painful reminder of the
		Holocaust and the atrocities committed during World War II.
	</p>
	<h2>Where does that leave us?</h2>
	<p>
		As we come together to celebrate love and unity, we deeply respect and recognize the sensitivities
		around this symbol. Our hope is to provide a space of understanding, where two ancient cultures
		come together in joy and respect.
	</p>
